func TestCovModeOrSignal(t *testing.T) {
	t.Run("nil error returns nil regardless of ctx state", func(t *testing.T) {
		if err := orSignal(context.Background(), nil, "op"); err != nil {
			t.Errorf("orSignal(live, nil) = %v, want nil", err)
		}
		ctx, cancel := context.WithCancel(context.Background())
		cancel()
		if err := orSignal(ctx, nil, "op"); err != nil {
			t.Errorf("orSignal(cancelled, nil) = %v, want nil", err)
		}
	})

	t.Run("a cancelled ctx with an error is a clean signal stop", func(t *testing.T) {
		ctx, cancel := context.WithCancel(context.Background())
		cancel()
		err := orSignal(ctx, errors.New("boom"), "run agent")
		if !errors.Is(err, errSignaled) {
			t.Errorf("orSignal(cancelled, err) = %v, want errSignaled", err)
		}
	})

	t.Run("a live ctx wraps the error with the op label", func(t *testing.T) {
		base := errors.New("boom")
		err := orSignal(context.Background(), base, "run agent")
		if err == nil {
			t.Fatal("orSignal(live, err) = nil, want the wrapped error")
		}
		if !errors.Is(err, base) {
			t.Errorf("wrapped error should unwrap to the base error, got %v", err)
		}
		if errors.Is(err, errSignaled) {
			t.Error("a live-ctx error must not be reported as errSignaled")
		}
		if !strings.HasPrefix(err.Error(), "run agent: ") {
			t.Errorf("error = %q, want it prefixed with the op", err.Error())
		}
	})
}
