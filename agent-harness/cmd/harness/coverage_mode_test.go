func TestCovModeIssueErrors(t *testing.T) {
	// fetchIssue succeeds here; each case exercises a post-fetch required-input
	// check in the PR (non-issue-output) issue-mode arm.
	covModeFakeIssueGh(t)

	cases := []struct {
		name    string
		cfg     config
		wantSub string
	}{
		{
			name:    "empty CLAUDE_TASK",
			cfg:     config{issueNumber: "42", task: "", agentBranch: "b", systemPrompt: "s", outputType: "pr"},
			wantSub: "CLAUDE_TASK is empty",
		},
		{
			name:    "empty AGENT_BRANCH",
			cfg:     config{issueNumber: "42", task: "x", agentBranch: "", systemPrompt: "s", outputType: "pr"},
			wantSub: "AGENT_BRANCH is empty",
		},
		{
			name:    "empty CLAUDE_SYSTEM_PROMPT",
			cfg:     config{issueNumber: "42", task: "x", agentBranch: "b", systemPrompt: "", outputType: "pr"},
			wantSub: "CLAUDE_SYSTEM_PROMPT is empty",
		},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			cfg := tc.cfg
			cfg.workDir = t.TempDir()
			_, err := resolveMode(context.Background(), &cfg)
			if err == nil {
				t.Fatalf("resolveMode should error on %s", tc.name)
			}
			if !strings.Contains(err.Error(), tc.wantSub) {
				t.Errorf("error = %q, want it to mention %q", err.Error(), tc.wantSub)
			}
		})
	}
}

func TestCovModeIssueFetchError(t *testing.T) {
	// A non-zero gh exit propagates as a "fetch issue" error before any of the
	// required-input checks run.
	covGHFakeGh(t, "#!/bin/sh\necho 'gh: not authenticated' >&2\nexit 1\n")
	cfg := config{issueNumber: "42", task: "x", agentBranch: "b", systemPrompt: "s", outputType: "pr", workDir: t.TempDir()}
	_, err := resolveMode(context.Background(), &cfg)
	if err == nil {
		t.Fatal("resolveMode should error when gh fails")
	}
	if !strings.Contains(err.Error(), "fetch issue") {
		t.Errorf("error = %q, want it wrapped as a fetch-issue failure", err.Error())
	}
}

