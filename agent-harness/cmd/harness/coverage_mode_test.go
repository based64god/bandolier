// covModeIssueJSON is the `gh issue view` payload the fake gh echoes for issue
// #42, so resolveMode's fetchIssue resolves without a real gh/GitHub.
const covModeIssueJSON = `{"number":42,"title":"Fix the flaky test","body":"It fails intermittently."}`

// covModeFakeIssueGh installs a fake `gh` on PATH that echoes covModeIssueJSON,
// letting resolveMode fetch issue #42. Reuses covGHFakeGh from the github
// coverage tests rather than duplicating the PATH-shim plumbing.
func covModeFakeIssueGh(t *testing.T) {
	t.Helper()
	covGHFakeGh(t, "#!/bin/sh\necho '"+covModeIssueJSON+"'\n")
}

// covModeRepoURL is a throwaway repo URL: resolveMode only switches on whether
// repoURL is set (it never clones), so any non-empty value selects repo mode.
const covModeRepoURL = "https://example.com/owner/repo.git"

func TestCovModePlainMode(t *testing.T) {
	t.Run("pr output: default plain run, no branch, issueOutput false", func(t *testing.T) {
		cfg := config{outputType: "pr"}
		mode, err := resolveMode(context.Background(), &cfg)
		if err != nil {
			t.Fatalf("resolveMode: %v", err)
		}
		if mode.prBranch != "" {
			t.Errorf("prBranch = %q, want empty in plain mode", mode.prBranch)
		}
		if mode.issueOutput {
			t.Error("issueOutput = true, want false for pr output")
		}
		if mode.issueOutput != cfg.issueOutput() {
			t.Errorf("issueOutput %v does not mirror cfg.issueOutput() %v", mode.issueOutput, cfg.issueOutput())
		}
		if cfg.systemPrompt != "" {
			t.Errorf("plain mode set a system prompt: %q", cfg.systemPrompt)
		}
	})

	t.Run("issue output: issueOutput mirrors cfg.issueOutput(), still no framing", func(t *testing.T) {
		cfg := config{outputType: "issue"}
		mode, err := resolveMode(context.Background(), &cfg)
		if err != nil {
			t.Fatalf("resolveMode: %v", err)
		}
		if !mode.issueOutput {
			t.Error("issueOutput = false, want true for issue output")
		}
		if mode.issueOutput != cfg.issueOutput() {
			t.Errorf("issueOutput %v does not mirror cfg.issueOutput() %v", mode.issueOutput, cfg.issueOutput())
		}
		if mode.prBranch != "" {
			t.Errorf("prBranch = %q, want empty in plain mode", mode.prBranch)
		}
		// The plain (default) switch arm builds no framing even for issue output.
		if cfg.systemPrompt != "" {
			t.Errorf("plain mode set a system prompt: %q", cfg.systemPrompt)
		}
	})
}

func TestCovModeRepoFreshPR(t *testing.T) {
	t.Run("titled fresh run cuts a slugged branch and frames the repo prompt", func(t *testing.T) {
		cfg := config{repoURL: covModeRepoURL, title: "Add feature", outputType: "pr"}
		mode, err := resolveMode(context.Background(), &cfg)
		if err != nil {
			t.Fatalf("resolveMode: %v", err)
		}
		re := regexp.MustCompile(`^bandolier/add-feature-[0-9a-z]{6}$`)
		if !re.MatchString(mode.prBranch) {
			t.Errorf("prBranch = %q, want match %v", mode.prBranch, re)
		}
		if mode.prTitle != "Bandolier agent changes" {
			t.Errorf("prTitle = %q, want placeholder", mode.prTitle)
		}
		if mode.prBody != "Generated by Bandolier." {
			t.Errorf("prBody = %q", mode.prBody)
		}
		if mode.issueOutput {
			t.Error("issueOutput = true, want false for pr output")
		}
		if want := buildRepoSystemPrompt(mode.prBranch); cfg.systemPrompt != want {
			t.Errorf("systemPrompt = %q, want buildRepoSystemPrompt(prBranch)", cfg.systemPrompt)
		}
	})

	t.Run("empty title falls back to the task branch slug", func(t *testing.T) {
		cfg := config{repoURL: covModeRepoURL, title: "", outputType: "pr"}
		mode, err := resolveMode(context.Background(), &cfg)
		if err != nil {
			t.Fatalf("resolveMode: %v", err)
		}
		re := regexp.MustCompile(`^bandolier/task-[0-9a-z]{6}$`)
		if !re.MatchString(mode.prBranch) {
			t.Errorf("prBranch = %q, want the task-slug fallback %v", mode.prBranch, re)
		}
	})
}

